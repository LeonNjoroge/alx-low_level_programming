#include "main.h"

/**
 * add - Adds two integers.
 *
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Return: The sum of @a and @b.
 *
 * Description: This function takes two integers @a and @b as input and returns
 * their sum. It calculates the sum and returns it.
 */
int add(int a, int b)
{
	int sum = a + b;
	return (sum);
}
