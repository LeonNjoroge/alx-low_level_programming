/**
 * mul - multiplies two integers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Description: This function multiplies 
 *two integers and returns the results
 * Return: The product of a and b.
 */
int mul(int a, int b)
{
    int result;

    result = a * b;
    return (result);
}
